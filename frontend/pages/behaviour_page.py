    def _shade_dark(fig, light_on, t_rel):
        ds = np.where(np.diff(light_on.astype(int)) == -1)[0]
        de = np.where(np.diff(light_on.astype(int)) == 1)[0]
        if not light_on[0]:
            ds = np.concatenate([[0], ds])
        if not light_on[-1]:
            de = np.concatenate([de, [len(light_on) - 1]])
        for s_, e_ in zip(ds, de):
            fig.add_vrect(
                x0=t_rel[s_], x1=t_rel[min(e_, len(t_rel) - 1)],
                fillcolor="gray", opacity=0.15, line_width=0,
            )

    # Speed trace
    fig_sp = go.Figure()
    fig_sp.add_trace(go.Scatter(
        x=t_tr, y=sess_tr["speed_cm_s"],
        mode="lines", line=dict(width=0.8, color="steelblue"), name="Speed",
    ))
    _shade_dark(fig_sp, sess_tr["light_on"], t_tr)
    fig_sp.update_layout(
        height=250, template="plotly_white",
        xaxis_title="Time (s)", yaxis_title="Speed (cm/s)",
        title="Speed", margin=dict(l=50, r=20, t=35, b=30),
    )
