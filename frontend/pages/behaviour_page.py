    if not light_on[-1]:
        dark_ends = np.concatenate([dark_ends, [len(light_on) - 1]])
    for ds, de in zip(dark_starts, dark_ends):
        fig_speed_time.add_vrect(
            x0=t_rel[ds], x1=t_rel[min(de, len(t_rel) - 1)],
            fillcolor="gray", opacity=0.15, line_width=0,
        )
    fig_speed_time.update_layout(
        template="plotly_white", height=350,
        xaxis_title="Time (s)", yaxis_title="Speed (cm/s)",
        title=f"Speed trace: {sess['exp_id']}",
    )
    st.plotly_chart(fig_speed_time, use_container_width=True, key="behav_fig_speed_time")

    # Active/inactive bout duration distributions
    st.markdown("#### Active/Inactive Bout Durations")
    all_active_bouts = []
    all_inactive_bouts = []
    for s in sessions:
        good = ~s["bad_behav"]
        active = s["active"] & good
        dt = np.median(np.diff(s["frame_times"])) if len(s["frame_times"]) > 1 else 1.0
        # Find bouts via run-length encoding
        changes = np.diff(active.astype(int))
        starts = np.where(changes == 1)[0] + 1
        ends = np.where(changes == -1)[0] + 1
        if active[0]:
            starts = np.concatenate([[0], starts])
        if active[-1]:
            ends = np.concatenate([ends, [len(active)]])
        for bs, be in zip(starts, ends):
            all_active_bouts.append((be - bs) * dt)

        # Inactive bouts
        inactive = ~s["active"] & good
        changes_i = np.diff(inactive.astype(int))
        starts_i = np.where(changes_i == 1)[0] + 1
        ends_i = np.where(changes_i == -1)[0] + 1
        if inactive[0]:
            starts_i = np.concatenate([[0], starts_i])
        if inactive[-1]:
            ends_i = np.concatenate([ends_i, [len(inactive)]])
        for bs, be in zip(starts_i, ends_i):
            all_inactive_bouts.append((be - bs) * dt)

    col_b1, col_b2 = st.columns(2)
    with col_b1:
        if all_active_bouts:
            fig_ab = px.histogram(
                x=all_active_bouts, nbins=60,
                labels={"x": "Bout Duration (s)", "y": "Count"},
                title="Active Bout Durations",
            )
            fig_ab.update_layout(template="plotly_white", height=350)
            st.plotly_chart(fig_ab, use_container_width=True, key="behav_fig_active_bouts")
            st.caption(
                f"N = {len(all_active_bouts)} bouts, "
                f"median = {np.median(all_active_bouts):.2f} s"
            )
    with col_b2:
        if all_inactive_bouts:
            fig_ib = px.histogram(
                x=all_inactive_bouts, nbins=60,
                labels={"x": "Bout Duration (s)", "y": "Count"},
                title="Inactive Bout Durations",
            )
            fig_ib.update_layout(template="plotly_white", height=350)
            st.plotly_chart(fig_ib, use_container_width=True, key="behav_fig_inactive_bouts")
            st.caption(
                f"N = {len(all_inactive_bouts)} bouts, "
                f"median = {np.median(all_inactive_bouts):.2f} s"
            )


