# ── Helper: compute per-session behavioural summary ──────────────────────
def _session_summary(s: dict) -> dict:
    """Compute behavioural summary stats for a single session."""
    ft = s["frame_times"]
    speed = s["speed_cm_s"]
    hd = s["hd_deg"]
    light_on = s["light_on"]
    active = s["active"]
    bad = s["bad_behav"]
    good = ~bad

    # Time
    dt = np.median(np.diff(ft)) if len(ft) > 1 else 1.0
    total_dur = ft[-1] - ft[0] if len(ft) > 1 else 0.0
    active_frac = float(np.mean(active[good])) if good.any() else 0.0

    # Speed (good frames only)
    good_speed = speed[good]
    mean_speed = float(np.nanmean(good_speed)) if len(good_speed) > 0 else 0.0
    median_speed = float(np.nanmedian(good_speed)) if len(good_speed) > 0 else 0.0

    # Distance
    distance_cm = float(np.nansum(speed[good] * dt))

    # Light/dark time
    light_time = float(np.sum(light_on[good]) * dt)
    dark_time = float(np.sum(~light_on[good]) * dt)

    # Angular head velocity
    hd_rad = np.deg2rad(hd)
    dhd = np.diff(hd_rad)
    # Unwrap angular differences to [-pi, pi]
    dhd = np.arctan2(np.sin(dhd), np.cos(dhd))
    ahv = np.abs(dhd) / dt  # rad/s -> convert to deg/s
    ahv_deg = np.rad2deg(ahv)
    # Only good frames (ahv has length n-1, so use good[1:] & good[:-1])
    good_ahv_mask = good[1:] & good[:-1]
    mean_ahv = float(np.nanmean(ahv_deg[good_ahv_mask])) if good_ahv_mask.any() else 0.0

    # Light vs dark speed
    light_mask = light_on & good
    dark_mask = ~light_on & good
    speed_light = float(np.nanmean(speed[light_mask])) if light_mask.any() else np.nan
    speed_dark = float(np.nanmean(speed[dark_mask])) if dark_mask.any() else np.nan

    # Light vs dark active fraction
    active_light = float(np.mean(active[light_mask])) if light_mask.any() else np.nan
    active_dark = float(np.mean(active[dark_mask])) if dark_mask.any() else np.nan

    # Light vs dark AHV
    light_ahv_mask = light_on[1:] & light_on[:-1] & good_ahv_mask
    dark_ahv_mask = ~light_on[1:] & ~light_on[:-1] & good_ahv_mask
    ahv_light = float(np.nanmean(ahv_deg[light_ahv_mask])) if light_ahv_mask.any() else np.nan
    ahv_dark = float(np.nanmean(ahv_deg[dark_ahv_mask])) if dark_ahv_mask.any() else np.nan

    return {
        "exp_id": s["exp_id"],
        "animal_id": s["animal_id"],
        "celltype": s["celltype"],
        "total_dur_s": total_dur,
        "active_frac": active_frac,
        "mean_speed": mean_speed,
        "median_speed": median_speed,
        "distance_cm": distance_cm,
        "light_time_s": light_time,
        "dark_time_s": dark_time,
        "mean_ahv_deg_s": mean_ahv,
        "speed_light": speed_light,
        "speed_dark": speed_dark,
        "active_light": active_light,
        "active_dark": active_dark,
        "ahv_light": ahv_light,
        "ahv_dark": ahv_dark,
    }


